/** \brief suma de dos valores.
 *
 * \param a int Primer valor a sumar.
 * \param b int Segundo valor a sumar.
 * \return int resultado de la suma.
 */
int suma(int num1, int num2);

/** \brief resta de dos valores
 *
 * \param num1 primer valor a restar
 * \param num2 segundo valor a restar
 * \return int Resultado de la resta */
int resta(int num1, int num2);

/** \brief producto de dos valores
 *
 * \param num1 int primer valor del producto
 * \param num2 int segundo valor del producto
 * \return int resultado del producto */
int producto(int num1, int num2);

/** \brief division entre dos valores
 *
 * \param num1 dividendo
 * \param num2 divisor
 * \return float division entre dos numeros  */
float division(int num1, int num2);

/** \brief Calcula el factorial de un numero
 *
 * \param num1 int numero para calcular
 * \return int factorial del numero deseado  */
int factorial(int num1);

